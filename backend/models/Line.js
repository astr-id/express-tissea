const mongoose = require("mongoose");

const lineSchema = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
    },
    start_time: {
      type: String,
      required: true,
    },
    end_time: {
      type: String,
      required: true,
    },
    category: {
      type: mongoose.Schema.Types.ObjectId,
      ref: "Category",
      required: true,
    },
  },
  { timestamps: true }
);

const Line = mongoose.model("Line", lineSchema);
module.exports = Line;
